#!/usr/bin/env python3
'''Module for converting a floating-point number to a string.
'''


def to_str(n: float) -> str:
    '''Converts the input floating-point number to its string representation.

    Args:
        n (float): The input floating-point number.

    Returns:
        str: The string representation of the input floating-point number.
    '''
    return str(n)
